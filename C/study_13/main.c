	// 截断1111 1111 因为是无符号字符,所以整型提升时符号位直接补0
	// 0000 0000 0000 0000 0000 0000 1111 1111 -正数补码=原码
	printf("d=%d,e=%d,f=%d\n",d,e,f);
	
