	printf("%u\n",g); // 4294967168 %u打印无符号整数，因为是打印无符号整数，所以打印时符号位的1不代表负号，所以整型提升后的数相当于正数
	
	if((strlen("asd") - strlen("asdsa")) >= 0){
		printf(">\n"); // 打印，因为strlen返回的类型时size_t，是无符号整型unsigned int
	}
	else{
		printf("<\n");
	}
	
	int count = mystrlen("asdaf");
	printf("%d\n",count);
	
	/* 
		2、浮点数在内存中的存储
	*/
	
	float h = 5.5;
	/* 
		// 将浮点数存入内存
		
		1、5.5 写成二进制是 (5 和 0.5 两个部分) -> 5是101，0.5是2^(-1)，所以是0.1，因此5.5二进制是：
			101.1 -> 1*2^2 + 0 + 1*2^0 + 1*2^(-1) = 5.5
		2、换成IEEE754格式
			(-1)^0 * 1.011 * 2^2 -> S = 0; M = 1.011; E = 2
		3、写成内存中的二进制形式
			S = 0	E = 2,E + 127 = 129		M = 1.011,
			0  		10000001(129)			01100000000000000000000(只写小数位,不足位数自动向右补0)
			二进制效果:	01000000101100000000000000000000
			即			0100 0000 1011 0000	0000 0000 0000 0000
			写成十六进制:	4      0    b    0    0    0    0    0	
			即				0x40 b0 00 00 -> 内存中显示(内存中需换小端显示即 00 00 b0 40)	
		
		#############################################################################
		
		// 从内存中读取
		
		// 主要时E的三种情况：1、不全为0或不全为1；2、E全为0；3、E全为1
		// 主要讨论指数 E 不全为0或不全为1的情况
		
		1、内存中二进制序列：0100 0000 1011 0000 0000 0000 0000 0000
		2、取出E的8bit： 10000001 -> 129
		3、E - 127得到真实值：	129 - 127 = 2
		4、还原M的小数位，即后23位：011 00000000000000000000 -> 011
		5、S = 0
		6、再将M的首位加1： (-1)^0 * 1.011 * 2^2 = 101.1
		
	*/

